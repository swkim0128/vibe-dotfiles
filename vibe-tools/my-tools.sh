#!/bin/bash

COMMANDS_FILE="$HOME/.config/vibe-tools/commands.txt"

# ---------------------------------------------------------
# [1] 명령어 목록 파일 존재 여부 확인
# ---------------------------------------------------------
if [ ! -f "$COMMANDS_FILE" ]; then
    echo -e "\033[0;31m[오류]\033[0m 명령어 목록 파일이 없습니다: $COMMANDS_FILE"
    exit 1
fi

# ---------------------------------------------------------
# [2] 파일에서 주석(#)과 빈 줄을 제거하고 fzf로 선택
# ---------------------------------------------------------
SELECTED=$(grep -v '^\s*#' "$COMMANDS_FILE" | grep -v '^\s*$' | fzf \
  --prompt="🚀 실행할 작업을 선택하세요 > " \
  --height=40% --layout=reverse --border=rounded)

if [ -z "$SELECTED" ]; then
    exit 0
fi

# ---------------------------------------------------------
# [3] '|' 뒤의 실제 명령어를 추출하여 실행
# ---------------------------------------------------------
CMD=$(echo "$SELECTED" | awk -F ' \\| ' '{print $2}')

echo -e "\033[1;32m실행 중:\033[0m $CMD"
eval "$CMD"
